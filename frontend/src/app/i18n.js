const LanguageContext = createContext();

export const LanguageProvider = ({ children }) => {
  const [locale, setLocale] = useState('en');

  // Load language preference from browser / local storage
  useEffect(() => {
    const savedLang = localStorage.getItem('ec_app_lang');
    if (savedLang && i18nDictionary[savedLang]) {
      setLocale(savedLang);
    } else {
      const browserLang = navigator.language ? navigator.language.substring(0, 2) : 'en';
      if (i18nDictionary[browserLang]) {
        setLocale(browserLang);
      }
    }
  }, []);

  const changeLanguage = (lang) => {
    if (i18nDictionary[lang]) {
      setLocale(lang);
      localStorage.setItem('ec_app_lang', lang);
    }
  };

  const t = (key) => {
    return i18nDictionary[locale]?.[key] || i18nDictionary['en']?.[key] || key;
  };

  return (
    <LanguageContext.Provider value={{ locale, changeLanguage, t, languages: Object.keys(i18nDictionary) }}>
      {children}
    </LanguageContext.Provider>
  );
};

export const useTranslation = () => useContext(LanguageContext);
